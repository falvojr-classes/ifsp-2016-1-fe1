package br.edu.ifsp.fe1.entidades;

/**
 * Entidade que representa um Clube.
 *
 * @author falvojr
 */
public class Clube {

    private String nome;
    private double saldo;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }

}
